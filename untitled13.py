"""BERT NSP (Next Sentence  prediction)"""

import torch
from transformers import BertTokenizer, BertForNextSentencePrediction

# ------------------ LOAD MODEL ------------------
tokenizer = BertTokenizer.from_pretrained('bert-base-uncased')
model = BertForNextSentencePrediction.from_pretrained('bert-base-uncased')

# ------------------ INPUT ------------------
sentence_A = input("Enter Sentence A: ")
sentence_B = input("Enter Sentence B: ")

# ------------------ TOKENIZATION ------------------
inputs = tokenizer(sentence_A, sentence_B, return_tensors='pt')

# ------------------ PREDICTION ------------------
outputs = model(**inputs)
logits = outputs.logits

# Convert to probabilities
probs = torch.softmax(logits, dim=1)

# ------------------ OUTPUT ------------------
is_next_prob = probs[0][0].item()     # Correct
not_next_prob = probs[0][1].item()   # Correct

print("\nIsNext Probability:", is_next_prob)
print("NotNext Probability:", not_next_prob)

# Final decision
prediction = torch.argmax(probs, dim=1).item()

if prediction == 0:
    print("Prediction: Sentence B follows Sentence A ✅")
else:
    print("Prediction: Sentence B does NOT follow Sentence A ❌")

