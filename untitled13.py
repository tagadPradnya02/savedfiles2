nlp = spacy.load("en_core_web_sm")

def temporal_extraction(text):

    doc = nlp(text)

    # -------- Extract Time Expressions (spaCy NER) --------
    time_entities = []
    for ent in doc.ents:
        if ent.label_ in ["DATE", "TIME"]:
            time_entities.append((ent.text, ent.label_))

    # -------- Extract Events (Verbs) --------
    events = []
    for token in doc:
        if token.pos_ == "VERB":
            events.append((token.text, token.lemma_))

    # -------- Event-Time Linking (Simple Rule Based) --------
    event_time_pairs = []
    for event in events:
        for time in time_entities:
            event_time_pairs.append((event[0], time[0]))

    return time_entities, events, event_time_pairs

